a = 4
b = 11
print("binary 4 :", format(a, '08b'))
print("binary 11 :", format(b, '08b'))

print("\n")
# 4 | 11
c = a | b
print("nilai dari 4 | 11 adalah: ", c)

# 4 >> 11
c = a >> b
print("nilai dari 4 >> 11 adalah: ", c)

# 4 ^ 11
c = a ^ b
print("nilai dari 4 ^ 11 adalah: ", c)

# ~4
c = ~a
print("nilai dari ~4 adalah: ", c)

# 4 & 11
c = a & b
print("nilai dari 4 & 11 adalah: ", c)